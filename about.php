<p><b>President's College - Minuwangoda</b> (formerly known as <b>Japalawaththa Central College</b>) was founded on 09<sup>th</sup>  February 1979 by late Hon Bennet Gunasekara, Member of Parliament. The founder Principal was Mr. R.L.P.P. Rajapakse who led the school to be one of the most reputed and leading government schools in the Island.</p>
<p>The college was upgraded as 1A mix school on 1<sup>st</sup> September 1979.</p>
<h3>Admission Policy</h3>
<p>President’s College conduct class from Year 6- Year 13 and has usually admit 200 pupils onto Year 6. To be able to seek admission onto President’s college, student should sit for Year 5 National Scholarship exam conduced by the Department of Education of Sri Lanka and should score cut-off marks level or above. The College has very strict admission policy which allow only pupils who score higher marks at the National Scholarship exams to enter to Year 6. Currently there are about 1900 pupils studying at the school.</p>
<h3>Academic Excellence</h3>
<p>The college is proud to maintain an extremely high O/L and A/L results since 1983 upto date. The school has been recognized by both local authorities and National Ministry of Education as an Outstanding School in terms of academic excellence and in particular O/L and A/L results.</p>
<h3>Key Highlights of Academic Excellence of President’s College</h3>
<p>
 - President’s College is placed at TOP BEST 50 Schools out of 9500 Schools in Sri Lanka for excellent O/L results ( This is continued for each academic year)
 - President’s College is the No 1 School in Western Province of Sri Lanka for Best O/L results ( This is a continued Achievement )<br/>
 - 90 to 100 A/L students of President’s College gain entry onto Sri Lankan State universities each year which is outstanding.
</p>
<h3>Co-curricular activities</h3>
<p>All of students of the college are academically ambitious . President’s College offers boys and girls an extensive range of co-curricular activities, non-sporting as well as sporting. All boys and girls are encouraged to participate and to try out new activities.<p>

